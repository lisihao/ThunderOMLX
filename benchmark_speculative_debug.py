#!/usr/bin/env python3
"""
Speculative Decoding Debug Benchmark

Tracks acceptance rate and performance metrics to diagnose why SD is slow
"""

import argparse
import time
from mlx_lm import load, stream_generate


def benchmark_with_acceptance_tracking(
    model_path: str,
    draft_model_path: str,
    prompt_length: int,
    gen_length: int,
    num_speculative_tokens: int = 4,
):
    """
    Benchmark with detailed acceptance rate tracking
    """

    print("=" * 80)
    print("🔍 Speculative Decoding Debug Benchmark")
    print("=" * 80)

    # Configuration
    print(f"\n📊 Configuration:")
    print(f"   Target Model: {model_path}")
    print(f"   Draft Model:  {draft_model_path}")
    print(f"   Speculative Tokens (K): {num_speculative_tokens}")
    print(f"   Prompt Length: {prompt_length} tokens")
    print(f"   Generation Length: {gen_length} tokens")

    # Load models
    print(f"\n⏳ Loading target model...")
    model, tokenizer = load(model_path)
    print("✅ Target model loaded")

    print(f"\n⏳ Loading draft model from {draft_model_path}...")
    draft_model, _ = load(draft_model_path)
    print("✅ Draft model loaded")

    # Generate prompt
    print(f"\n⏳ Generating {prompt_length} token prompt...")
    base_text = "This is a comprehensive test prompt for benchmarking. "
    prompt = base_text * (prompt_length // len(base_text.split()) + 1)
    tokens = tokenizer.encode(prompt)
    if len(tokens) > prompt_length:
        tokens = tokens[:prompt_length]
    prompt = tokenizer.decode(tokens)
    print(f"✅ Prompt: {len(tokens)} tokens")

    # Warmup
    print("\n🔥 Warmup (1 run)...")
    _ = "".join(
        [
            r.text
            for r in stream_generate(
                model,
                tokenizer,
                prompt,
                max_tokens=32,
                draft_model=draft_model,
                num_draft_tokens=num_speculative_tokens,
            )
        ]
    )
    print("   ✓")

    # Run benchmark with detailed tracking
    print(f"\n📊 Running benchmark with acceptance tracking...")
    print("-" * 80)

    start_time = time.perf_counter()

    total_tokens = 0
    from_draft_count = 0
    from_target_count = 0

    for response in stream_generate(
        model,
        tokenizer,
        prompt,
        max_tokens=gen_length,
        draft_model=draft_model,
        num_draft_tokens=num_speculative_tokens,
    ):
        total_tokens += 1
        if response.from_draft:
            from_draft_count += 1
        else:
            from_target_count += 1

    end_time = time.perf_counter()
    total_time = end_time - start_time

    # Calculate metrics
    generation_tps = total_tokens / total_time
    acceptance_rate = from_draft_count / total_tokens if total_tokens > 0 else 0

    print("-" * 80)
    print(f"\n📈 Results:")
    print(f"   Total Time: {total_time:.3f}s")
    print(f"   Total Tokens: {total_tokens}")
    print(f"   Generation TPS: {generation_tps:.1f} tok/s")
    print(f"\n⭐ Acceptance Metrics:")
    print(f"   Accepted from Draft: {from_draft_count} ({acceptance_rate*100:.1f}%)")
    print(f"   Generated by Target: {from_target_count}")
    print(f"   Expected Speedup: {1 + acceptance_rate * num_speculative_tokens:.2f}x")

    # Diagnosis
    print(f"\n🔍 Diagnosis:")
    if acceptance_rate < 0.3:
        print(f"   ⚠️  POOR acceptance rate ({acceptance_rate*100:.1f}%)")
        print(f"   → Draft model quality is too low")
        print(f"   → Try different draft model or reduce num_speculative_tokens")
    elif acceptance_rate < 0.6:
        print(f"   ⚠️  MODERATE acceptance rate ({acceptance_rate*100:.1f}%)")
        print(f"   → Some benefit but not optimal")
        print(f"   → Consider tuning num_speculative_tokens")
    else:
        print(f"   ✅ GOOD acceptance rate ({acceptance_rate*100:.1f}%)")
        print(f"   → Draft model quality is good")

    print("\n" + "=" * 80)

    return generation_tps, acceptance_rate


def main():
    parser = argparse.ArgumentParser(description="Speculative Decoding Debug")
    parser.add_argument(
        "--model",
        type=str,
        default="/Users/lisihao/models/qwen3.5-35b-mlx",
        help="Target model path",
    )
    parser.add_argument(
        "--draft-model",
        type=str,
        required=True,
        help="Draft model path",
    )
    parser.add_argument(
        "--prompt-length",
        type=int,
        default=2048,
        help="Prompt length in tokens",
    )
    parser.add_argument(
        "--gen-length",
        type=int,
        default=256,
        help="Generation length in tokens",
    )
    parser.add_argument(
        "--num-speculative-tokens",
        type=int,
        default=4,
        help="K (number of speculative tokens)",
    )

    args = parser.parse_args()

    benchmark_with_acceptance_tracking(
        model_path=args.model,
        draft_model_path=args.draft_model,
        prompt_length=args.prompt_length,
        gen_length=args.gen_length,
        num_speculative_tokens=args.num_speculative_tokens,
    )


if __name__ == "__main__":
    main()
